public class _13_Looping {
    public static void main(String[] args) {
        // For Loop
        for (int i = 0; i < 5; i++) {
            System.out.println("Perulangan ke-" + i);
        }

        // While Loop
        int i = 0;
        while (i < 5) {
            System.out.println("Perulangan ke-" + i);
            i++;
        }

        // Do While Loop
        int j = 0;
        do {
            System.out.println("Perulangan ke-" + j);
            j++;
        } while (j < 5);
    }
}
